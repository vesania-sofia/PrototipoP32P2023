    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Detalle");
        modelo.addColumn("Status");

        clsTipoPersona TipPersona = new clsTipoPersona();
        //VendedorDAO vendedorDAO = new VendedorDAO();
        List<clsTipoPersona> listaTipoPersona = TipPersona.getListadoTipoPersona();
        tablaTipUsuario.setModel(modelo);
        String[] dato = new String[10];
        for (int i = 0; i < listaTipoPersona.size(); i++) {
            dato[0] = Integer.toString(listaTipoPersona.get(i).getPerTipoId());
            dato[1] = listaTipoPersona.get(i).getTipPerDescripcion();
            dato[2] = listaTipoPersona.get(i).getTipPerEstatus();
            modelo.addRow(dato);
        }       
    }

